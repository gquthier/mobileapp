  /**
   * ‚úÖ Add a single recorded video to the upload queue
   */
  static async addRecordedVideoToQueue(
    videoUri: string,
    title: string,
    userId: string,
    chapterId?: string,
    duration?: number
  ): Promise<string> {
    console.log('üì• Adding recorded video to upload queue:', { videoUri, title, userId, chapterId });

    const itemId = `recorded_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;

    // ‚úÖ Extract relative path from absolute URI for recorded videos
    // Format: file:///.../.../Documents/video_backups/backup_1234567890.mov
    // ‚Üí Extract: video_backups/backup_1234567890.mov
    let relativeUri: string | undefined;
    if (videoUri.includes('video_backups/')) {
      const parts = videoUri.split('video_backups/');
      if (parts.length > 1) {
        relativeUri = `video_backups/${parts[1]}`;
        console.log('üìÇ Extracted relative path:', relativeUri);
      }
    }

    const newItem: ImportVideoItem = {
      id: itemId,
      uri: videoUri, // Keep absolute path for immediate use
      relativeUri: relativeUri, // ‚úÖ Store relative path for iOS container changes
      filename: `${title || 'Recording'}_${Date.now()}.mp4`,
      title: title || 'Recorded Video',
      status: 'pending',
      progress: 0,
      retryCount: 0,
      metadata: {
        isRecorded: true,
        chapterId,
        duration,
      },
    };

    // ‚úÖ Phase 3: Use ImportQueueManager
    ImportQueueManager.addItems([newItem]);
    await ImportQueueManager.saveState();
    ImportQueueManager.notifyListeners();

    // Start processing if not already running
    if (!ImportQueueManager.isQueueProcessing()) {
      this.processQueue();
    }

    return itemId;
  }

