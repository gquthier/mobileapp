  /**
   * Process the queue - uploads videos concurrently
   */
  private static async processQueue(): Promise<void> {
    console.log('========================================');
    console.log('üöÄ PROCESSQUEUE: Called');
    console.log('========================================');

    // ‚úÖ Phase 3: Use ImportQueueManager
    if (ImportQueueManager.isQueueProcessing()) {
      console.log('‚ö†Ô∏è Queue already processing, exiting');
      console.log('========================================');
      return;
    }

    ImportQueueManager.setProcessing(true);
    const queueLength = ImportQueueManager.getQueueLength();
    const currentIndex = ImportQueueManager.getCurrentIndex();

    console.log(`üìä Queue length: ${queueLength} items`);
    console.log(`üìä Current index: ${currentIndex}`);
    console.log(`üìä Concurrent uploads: ${this.CONCURRENT_UPLOADS}`);
    console.log('üì¢ Notifying listeners (isProcessing = true)...');
    ImportQueueManager.notifyListeners();

    try {
      // Process videos in batches
      let batchNumber = 0;
      while (ImportQueueManager.hasMoreItems()) {
        batchNumber++;
        console.log(`\n--- Batch ${batchNumber} (index ${ImportQueueManager.getCurrentIndex()}) ---`);

        // ‚úÖ Phase 3: Get batch from ImportQueueManager
        const batch = ImportQueueManager.getNextBatch(this.CONCURRENT_UPLOADS);

        console.log(`üì¶ Batch items: ${batch.length}`);
        batch.forEach((item, idx) => {
          console.log(`  ${idx + 1}. ${item.filename} (status: ${item.status}, id: ${item.id})`);
        });

        if (batch.length === 0) {
          console.log('‚ö†Ô∏è No pending/failed items in batch, skipping...');
          ImportQueueManager.incrementIndex(this.CONCURRENT_UPLOADS);
          continue;
        }

        // Process batch concurrently
        console.log(`üîÑ Processing ${batch.length} items concurrently...`);
        const results = await Promise.allSettled(
          batch.map(item => this.processItem(item))
        );

        console.log(`‚úÖ Batch ${batchNumber} completed:`);
        results.forEach((result, idx) => {
          if (result.status === 'fulfilled') {
            console.log(`  ‚úÖ Item ${idx + 1}: fulfilled`);
          } else {
            console.log(`  ‚ùå Item ${idx + 1}: rejected -`, result.reason);
          }
        });

        ImportQueueManager.incrementIndex(this.CONCURRENT_UPLOADS);
        console.log(`üíæ Saving queue state (currentIndex now ${ImportQueueManager.getCurrentIndex()})...`);
        await ImportQueueManager.saveState();
      }

      console.log('\n========================================');
      console.log('‚úÖ PROCESSQUEUE: All batches completed');
      console.log(`üìä Final Results: ${ImportQueueManager.getCompletedCount()} completed, ${ImportQueueManager.getFailedCount()} failed`);
      console.log('========================================');

    } catch (error) {
      console.error('========================================');
      console.error('‚ùå PROCESSQUEUE: Error processing queue');
      console.error('Error:', error);
      console.error('Error message:', error instanceof Error ? error.message : 'Unknown');
      console.error('Error stack:', error instanceof Error ? error.stack : 'No stack');
      console.error('========================================');
    } finally {
      console.log('üèÅ PROCESSQUEUE: Finally block - setting isProcessing = false');
      ImportQueueManager.setProcessing(false);
      ImportQueueManager.notifyListeners();
      console.log('‚úÖ PROCESSQUEUE: Completed');
    }
  }

