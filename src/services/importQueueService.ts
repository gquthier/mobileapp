  /**
   * ‚úÖ Clean up invalid items from the queue
   * Removes items with files that no longer exist (e.g., after iOS container changes)
   * Also removes completed items older than 7 days
   */
  static async cleanupInvalidBackups(): Promise<void> {
    try {
      // console.log('üßπ [CLEANUP] Starting queue cleanup...');
      const items = ImportQueueManager.getItems();
      let removedInvalid = 0;
      let removedOldCompleted = 0;
      const sevenDaysAgo = Date.now() - (7 * 24 * 60 * 60 * 1000);

      for (const item of items) {
        // ‚úÖ Remove old completed items (older than 7 days)
        if (item.status === 'completed') {
          // Extract timestamp from item.id (format: recorded_1760792452623_xxx or import_1760792452623_xxx)
          const timestampMatch = item.id.match(/(?:recorded|import)_(\d+)_/);
          if (timestampMatch) {
            const itemTimestamp = parseInt(timestampMatch[1], 10);
            if (itemTimestamp < sevenDaysAgo) {
              // console.log(`üóëÔ∏è [CLEANUP] Removing old completed item: ${item.filename} (age: ${Math.floor((Date.now() - itemTimestamp) / (24 * 60 * 60 * 1000))} days)`);
              ImportQueueManager.removeItem(item.id);
              removedOldCompleted++;
              continue;
            }
          }
        }

        // ‚úÖ Check if file exists for pending/failed recorded videos
        if (item.metadata?.isRecorded && (item.status === 'pending' || item.status === 'failed')) {
          let videoUri: string;

          // Reconstruct path from relative URI if available
          if (item.relativeUri) {
            const currentContainer = FileSystem.documentDirectory;
            videoUri = `${currentContainer}${item.relativeUri}`;
          } else {
            videoUri = item.uri;
          }

          try {
            const fileInfo = await FileSystem.getInfoAsync(videoUri);
            if (!fileInfo.exists) {
              console.log(`üóëÔ∏è [CLEANUP] Removing invalid item (file not found): ${item.filename}`);
              console.log(`   Path checked: ${videoUri}`);
              ImportQueueManager.removeItem(item.id);
              removedInvalid++;
            }
          } catch (error) {
            console.log(`üóëÔ∏è [CLEANUP] Removing invalid item (error checking file): ${item.filename}`);
            ImportQueueManager.removeItem(item.id);
            removedInvalid++;
          }
        }
      }

      if (removedInvalid > 0 || removedOldCompleted > 0) {
        await ImportQueueManager.saveState();
        // console.log(`‚úÖ [CLEANUP] Removed ${removedInvalid} invalid backup(s) and ${removedOldCompleted} old completed item(s)`);
      } else {
        // console.log('‚úÖ [CLEANUP] Queue is clean, no items removed');
      }

      ImportQueueManager.notifyListeners();
    } catch (error) {
      console.error('‚ùå [CLEANUP] Error cleaning up queue:', error);
    }
  }
